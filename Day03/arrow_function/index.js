// General Function [구분법]
function add(x, y) {
  return x + y;
}

function subtract(x, y) {
  return x - y;
}

// Arrow Function [신문법]
// () => {}
const add1 = (x, y) => {
  return x + y;
};

const subtract1 = (x, y) => {
  return x - y;
};

const icecream = (x) => {
  return `${x}맛 아이스크림`;
};
const array = (x, y, z) => {
  return [x, y, z];
};
const num = (x) => {
  return x % 2 === 0 ? "짝수" : "홀수";
};
